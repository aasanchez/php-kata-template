<?php

namespace Kata;

/**
 * Class Bowling
 */
class Calc
{

    public function __construct()
    {
    }

    /**
     * This function calculateAdd: Sum of two element
     *
     * @param float $first First element in the sum.
     * @param float $second Second element in the sum.
     *
     * @return float
     */
    public function addition(float $first, float $second): float
    {
        return ($first + $second);
    }
}
